public class HomeNoticeTipBean {
    private String id;
    private String tipsName;
    private String tipsImageUrl;
    private String toUrl;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTipsName() {
        return tipsName;
    }

    public void setTipsName(String tipsName) {
        this.tipsName = tipsName;
    }

    public String getTipsImageUrl() {
        return tipsImageUrl;
    }

    public void setTipsImageUrl(String tipsImageUrl) {
        this.tipsImageUrl = tipsImageUrl;
    }

    public String getToUrl() {
        return toUrl;
    }

    public void setToUrl(String toUrl) {
        this.toUrl = toUrl;
    }
}
